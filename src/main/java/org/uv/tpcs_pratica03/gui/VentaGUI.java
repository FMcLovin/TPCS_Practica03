    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ListaProductosDialog listaDialog = new ListaProductosDialog(null, true, (Producto selectedProduct) -> {

            DetalleVenta detalleVenta = new DetalleVenta();
            detalleVenta.setId_venta(venta.getId_venta());
            detalleVenta.setId_producto(selectedProduct.getId_producto());
            detalleVenta.setCantidad(1);
            detalleVenta.setDescripcion(selectedProduct.getDescripcion());
            detalleVenta.setPrecio(selectedProduct.getPrecio());

            dvd.save(detalleVenta);
            addDetalle(detalleVenta);

        });
        listaDialog.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        long clienteID = Integer.parseInt(jSpinner1.getValue().toString());
        cliente = cd.get(clienteID);

        jLabel5.setText(cliente.getNombre());

        jTextDate.setText(new Date().toString());

        venta = new Venta();
        venta.setFecha(new Date());
        venta.setId_cliente(clienteID);
        venta.setTotal(new BigDecimal(0));

        vd.save(venta);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        for (DetalleVenta dv : venta.getDetallesVenta()) {
            venta.setTotal(venta.getTotal().add(dv.getPrecio()));
        }

        vd.update(venta);

    }//GEN-LAST:event_jButton4ActionPerformed

    private void addDetalle(DetalleVenta detalleVenta) {
        // Obtener el modelo de la JTable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // AÃ±adir detalle producto a la tabla sin borrar los datos existentes
        Object[] rowData = {
            detalleVenta.getId_detalle_venta(),
            detalleVenta.getId_venta(),
            detalleVenta.getId_producto(),
            detalleVenta.getCantidad(),
            detalleVenta.getDescripcion(),
            detalleVenta.getPrecio()
        };
        model.addRow(rowData);
        
        venta.getDetallesVenta().add(detalleVenta);
        
    }

